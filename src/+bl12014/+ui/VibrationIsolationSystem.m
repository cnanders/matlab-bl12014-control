            this.uiVisTempSensors.uiFrame2.turnOn();
            this.uiVisTempSensors.uiFrame3.turnOn();
            this.uiVisTempSensors.uiFrame4.turnOn();
        end
        
        function disconnectDataTranslationMeasurPoint(this)
            
        end
        
