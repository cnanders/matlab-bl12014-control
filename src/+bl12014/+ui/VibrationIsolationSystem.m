            this.uiStage3.setDevice(device3);
            this.uiStage4.setDevice(device4);
            
            this.uiStage1.turnOn();
            this.uiStage2.turnOn();
            this.uiStage3.turnOn();
            this.uiStage4.turnOn();
            
            % FIXME
